    public void agregarPedido() {
        Clientes nuevoCliente = new Clientes();
        Pedido nuevoPedido = new Pedido();

        try {
            nuevoCliente.setNombre(jTextField2.getText());
            nuevoCliente.setNumeroDocumento(jTextField3.getText());

            Clientes clienteExistente = clienteServ.findClientesByIdentificacion(nuevoCliente.getNumeroDocumento());

            if (clienteExistente != null) {
                System.out.println("El cliente ya existe");
                return;
            }

            if (nuevoCliente.getNombre().isEmpty() || nuevoCliente.getNumeroDocumento().isEmpty()) {
                System.out.println("Debe digitar un nombre y una identificacion para el cliente");
                return;
            }

            clienteServ.addCliente(nuevoCliente);
            System.out.println("Cliente agregado");
        } catch (Exception e) {
            System.out.println("Error al agregar Cliente");
        }

        try {
            Clientes clienteGuardado = clienteServ.findClientesByIdentificacion(jTextField3.getText());
            int cantidad1 = jTextField4.getText().isEmpty() ? 0 : Integer.parseInt(jTextField4.getText());
            int cantidad2 = jTextField5.getText().isEmpty() ? 0 : Integer.parseInt(jTextField5.getText());
            int cantidad3 = jTextField6.getText().isEmpty() ? 0 : Integer.parseInt(jTextField6.getText());
            int cantidad4 = jTextField7.getText().isEmpty() ? 0 : Integer.parseInt(jTextField7.getText());
            nuevoPedido.setClientesId(clienteGuardado);
            nuevoPedido.setCantidadMenu1(cantidad1);
            nuevoPedido.setCantidadMenu2(cantidad2);
            nuevoPedido.setCantidadMenu3(cantidad3);
            nuevoPedido.setCantidadMenu4(cantidad4);
            nuevoPedido.setEstado(true);
            pedidoService.addPedido(nuevoPedido);
            limpiarCampos();
        } catch (Exception e) {
            System.out.println("Error al agregar pedido");
        }

    }

