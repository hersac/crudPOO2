    public TableModel obtenerTabla() {
        DefaultTableModel dtm = new DefaultTableModel();
        String[] headers = { "ID", "NOMBRE", "NRO. DOCUMENTO", "MENU 1", "MENU 2", "MENU 3",
                "MENU 4", "TOTAL" };
        dtm.setDataVector(listarPedidosTabla(), headers);
        return dtm;
    }

    public Object[][] listarPedidosTabla() {
        try {
            List<Pedido> pedidos = listarPedidos();
            List<Menu> menus = listarMenus();
            ArrayList<Double> preciosMenu = new ArrayList<>();

            menus.forEach((menu) -> {
                preciosMenu.add(menu.getPrecio());
            });
            Object[][] data = new Object[pedidos.size()][8];
            int i = 0;
            for (Pedido pedido : pedidos) {
                Double valorTotal = 0.0;

                data[i][0] = pedido.getPedidoId();
                data[i][1] = pedido.getClientesId().getNombre();
                data[i][2] = pedido.getClientesId().getNumeroDocumento();
                data[i][3] = pedido.getCantidadMenu1();
                data[i][4] = pedido.getCantidadMenu2();
                data[i][5] = pedido.getCantidadMenu3();
                data[i][6] = pedido.getCantidadMenu4();

                valorTotal = valorTotal + ValorCantidadxMenu(preciosMenu.get(0), pedido.getCantidadMenu1());
                valorTotal = valorTotal + ValorCantidadxMenu(preciosMenu.get(1), pedido.getCantidadMenu2());
                valorTotal = valorTotal + ValorCantidadxMenu(preciosMenu.get(2), pedido.getCantidadMenu3());
                valorTotal = valorTotal + ValorCantidadxMenu(preciosMenu.get(3), pedido.getCantidadMenu4());

                data[i][7] = formatearComoMoneda(valorTotal);
                i++;
            }
            return data;
        } catch (Exception e) {
            System.out.println("Error al listar pedidos");
            return null;
        }
    }

    private Double ValorCantidadxMenu(Double valor, int cantidad) {
        return valor * cantidad;
    }

    private String formatearComoMoneda(double valor) {
        DecimalFormat formatoMoneda = new DecimalFormat("#,##0.00");
        return "$ " + formatoMoneda.format(valor);
    }

