    public void eliminarPedido(Long id, String identificacion) {
        try {
            pedidoService.deletePedido(id);
            Clientes cliente = clienteServ.findClientesByIdentificacion(identificacion);
            clienteServ.deleteClienteById(cliente.getClientesId());
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

